#include <iostream>
using namespace std;

int main () {
    // Arithmetic Operators
    int a = 5;
    int b = 3;
    cout << "+ " << (a + b) << endl; //8
    cout << "- " << (a - b) << endl; //2
    cout << "* " << (a * b) << endl; //15
    cout << "/ " << (a / b) << endl; //1
    cout << "% " << (a % b) << endl; //2
    return 0;
}